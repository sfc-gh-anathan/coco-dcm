
  create or replace   view DBT_DCM_DEMO.MARTS_DEV.fct_payroll_volume_by_region
  
   as (
    SELECT
    c.REGION,
    c.PLAN_TYPE,
    DATE_TRUNC('MONTH', p.RUN_DATE) AS PAYROLL_MONTH,
    COUNT(DISTINCT p.PAYROLL_RUN_ID) AS TOTAL_RUNS,
    COUNT(DISTINCT p.CLIENT_ID) AS ACTIVE_CLIENTS,
    SUM(p.CHECK_COUNT) AS TOTAL_CHECKS_PROCESSED,
    SUM(p.TOTAL_GROSS_PAY) AS TOTAL_GROSS_PAYROLL,
    SUM(p.TOTAL_NET_PAY) AS TOTAL_NET_PAYROLL,
    SUM(p.TOTAL_DEDUCTIONS) AS TOTAL_DEDUCTIONS,
    AVG(p.TOTAL_GROSS_PAY) AS AVG_GROSS_PER_RUN
FROM DBT_DCM_DEMO.STAGING_DEV.stg_payroll_runs p
JOIN DBT_DCM_DEMO.STAGING_DEV.stg_clients c USING (CLIENT_ID)
WHERE p.STATUS = 'COMPLETED'
GROUP BY 1, 2, 3
  );

