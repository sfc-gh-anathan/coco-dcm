WITH source AS (
    SELECT * FROM DBT_DCM_DEMO.RAW_DEV.payroll_runs
)

SELECT
    PAYROLL_RUN_ID,
    CLIENT_ID,
    RUN_DATE,
    PAY_PERIOD_START,
    PAY_PERIOD_END,
    TOTAL_GROSS_PAY,
    TOTAL_NET_PAY,
    TOTAL_GROSS_PAY - TOTAL_NET_PAY AS TOTAL_DEDUCTIONS,
    CHECK_COUNT,
    UPPER(STATUS) AS STATUS,
    PROCESSED_AT,
    PAYMENT_METHOD
FROM source
WHERE STATUS IS NOT NULL